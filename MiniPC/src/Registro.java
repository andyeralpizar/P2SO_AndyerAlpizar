// Crea un registro 
public class Registro {
    private String nombre;
    private int valor;

    public Registro(String nombre) {
        this.nombre = nombre;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public String getNombre() {
        return nombre;
    }
    
    
}
